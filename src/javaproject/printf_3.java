package javaproject;

public class printf_3 {

	public static void main(String[] args) {
		
		
		float f1 = .10f; //0.11
		float f2 = 11f;		//11.0
		float f3 = 3.14f;	//3.14
		
		
		System.out.printf("f1 = %f\n",f1);
		System.out.printf("f1 = %.1f\n",f2);
		System.out.printf("f1 = %.2f\n",f3);
		
		
		
		
		
		
		
		
		
		
		
	}

}
