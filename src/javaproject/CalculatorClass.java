package javaproject;

public class CalculatorClass {

	
	public void powerOn() {
		System.out.println("계산기 전원을 켭니다.!!!");
	}
	
	public int plus(int x, int y) {
		return x+y;
	}
	
	
	public double divide(int x, int y) {
		return (double)x/y;
	}
	
	 public void powerOff() {
		System.out.println("계산기 전원을 끕니다.!!!");
	}
	
	
	
	
	
	
	
	
	
	
	
	
}
